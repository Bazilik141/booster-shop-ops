    $row = $stmt->get_result()->fetch_assoc();
    $stmt->close();

    if (!$row) {
        bs_fail('Offer row missing after update');
    }
    if ((string)$row['title'] !== OFFER_TITLE) {
        bs_fail('Offer title verification failed');
    }
    if (hash('sha256', (string)$row['description']) !== OFFER_HTML_SHA256_EXPECTED) {
        bs_fail('Offer description verification failed');
    }

    bs_log('changed_db', $table . ' information_id=' . $informationId . ' language_id=' . OFFER_LANGUAGE_ID);
    bs_log('offer_html_sha256', OFFER_HTML_SHA256_EXPECTED);
}

function bs_run(): void {
    $cwd = getcwd();
    if (!is_string($cwd) || $cwd === '') {
        bs_fail('Cannot determine cwd');
    }

    bs_log('patch', PATCH_NAME);
    bs_log('cwd', $cwd);
    bs_log('time', date('c'));

    $configPath = bs_join($cwd, 'config.php');
    if (!is_file($configPath)) {
        bs_fail('config.php not found. Run this patch from OpenCart public_html.');
    }
    require_once $configPath;

    $backupDir = bs_join($cwd, 'backup-r09fix-toc-offer-' . date('Ymd-His'));
    if (!is_dir($backupDir) && !mkdir($backupDir, 0755, true)) {
        bs_fail('Cannot create backup directory: ' . $backupDir);
    }
    bs_log('backup_dir', $backupDir);

    $offerHtml = bs_decode_offer_html();
    bs_log('offer_html_h2_count', (string)substr_count($offerHtml, '<h2>'));

    $tocChanged = bs_patch_toc($cwd, $backupDir);

    $db = bs_connect_db();
    try {
        $table = bs_safe_table((string)DB_PREFIX, 'information_description');
        $informationId = bs_find_offer_id($db, $table);
        bs_backup_offer_row($db, $table, $informationId, $backupDir);
        bs_update_offer($db, $table, $informationId, $offerHtml);
    } finally {
        $db->close();
    }

    bs_log('syntax_check', bs_syntax_check_self());
    bs_log('cache_clear', 'run separately after patch');
    bs_log('toc_changed', $tocChanged ? 'yes' : 'no');
    bs_log('done', 'ok');

    @unlink(__FILE__);
    bs_log('self_delete', file_exists(__FILE__) ? 'failed' : 'ok');
}

try {
    bs_run();
} catch (Throwable $e) {
    bs_log('error', $e->getMessage());
    bs_log('done', 'failed');
    exit(1);
}
