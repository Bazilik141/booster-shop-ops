    $stmt->bind_param('ssii', $title, $offerHtml, $informationId, $languageId);
    $stmt->execute();
    bs_log('db_rows_affected', (string)$stmt->affected_rows);
    $stmt->close();

    $stmt = $db->prepare('SELECT title, description FROM ' . $table . ' WHERE information_id = ? AND language_id = ? LIMIT 1');
    $stmt->bind_param('ii', $informationId, $languageId);
