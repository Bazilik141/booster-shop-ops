function bs_find_information_twig_candidates(string $cwd, string $old, string $new): array {
    $base = bs_join($cwd, 'catalog/view/template');
    if (!is_dir($base)) {
        bs_fail('Template base directory not found: ' . $base);
    }

    $all = [];
    $matches = [];
    $iterator = new RecursiveIteratorIterator(
        new RecursiveDirectoryIterator($base, FilesystemIterator::SKIP_DOTS)
    );

    foreach ($iterator as $file) {
        if (!$file->isFile() || $file->getFilename() !== 'information.twig') {
            continue;
        }

        $path = $file->getPathname();
        $relative = str_replace(DIRECTORY_SEPARATOR, '/', substr($path, strlen($cwd) + 1));
        $all[] = $relative;

        $content = file_get_contents($path);
        if (!is_string($content)) {
            continue;
        }

        if (strpos($content, $old) !== false || strpos($content, $new) !== false) {
            $matches[] = ['path' => $path, 'relative' => $relative, 'content' => $content];
        }
    }

    bs_log('information_twig_found', implode('|', $all));
    return $matches;
}

function bs_patch_toc(string $cwd, string $backupDir): bool {
    $old = "a.href = '#' + id;";
    $new = "a.href = window.location.href.replace(/#.*$/, '') + '#' + id;";

    $matches = bs_find_information_twig_candidates($cwd, $old, $new);
    if (count($matches) !== 1) {
        $summary = [];
        foreach ($matches as $match) {
            $summary[] = $match['relative'];
        }
        bs_fail('Cannot uniquely identify TOC information.twig. Matches=' . implode('|', $summary));
    }

    $path = $matches[0]['path'];
    $relative = $matches[0]['relative'];
    $content = $matches[0]['content'];
    bs_log('target_twig', $relative);

    if (strpos($content, $new) !== false && strpos($content, $old) === false) {
        bs_log('toc_fix', 'already_applied');
        return false;
    }

    $count = substr_count($content, $old);
    if ($count !== 1) {
        bs_fail('Expected exactly one TOC href line, found ' . $count . ' in ' . $relative);
    }

    bs_backup_file($path, $backupDir, $relative);
    $updated = str_replace($old, $new, $content, $replaceCount);
    if ($replaceCount !== 1) {
        bs_fail('TOC href replacement failed unexpectedly');
    }
    if (file_put_contents($path, $updated, LOCK_EX) === false) {
        bs_fail('Cannot write target Twig file: ' . $path);
    }

    bs_log('changed_file', $relative);
    return true;
}

function bs_connect_db(): mysqli {
    if (!extension_loaded('mysqli')) {
        bs_fail('mysqli extension is not loaded');
    }
    foreach (['DB_HOSTNAME', 'DB_USERNAME', 'DB_PASSWORD', 'DB_DATABASE', 'DB_PREFIX'] as $constant) {
        if (!defined($constant)) {
            bs_fail('Missing config constant: ' . $constant);
        }
    }

    mysqli_report(MYSQLI_REPORT_ERROR | MYSQLI_REPORT_STRICT);
    $port = defined('DB_PORT') ? (int)DB_PORT : 3306;
    $db = new mysqli(DB_HOSTNAME, DB_USERNAME, DB_PASSWORD, DB_DATABASE, $port);
    $db->set_charset('utf8mb4');
    bs_log('db_connect', 'ok');
    bs_log('db_table', DB_PREFIX . 'information_description');
    return $db;
}

function bs_find_offer_id(mysqli $db, string $table): int {
    $result = $db->query('SELECT information_id, title FROM ' . $table . ' WHERE language_id = ' . OFFER_LANGUAGE_ID . ' ORDER BY information_id');
    $exact = [];
    $partial = [];

    while ($row = $result->fetch_assoc()) {
        $id = (int)$row['information_id'];
        $title = (string)$row['title'];
        if (bs_lower(trim($title)) === bs_lower(OFFER_TITLE)) {
            $exact[] = ['id' => $id, 'title' => $title];
        } elseif (bs_contains_offer($title)) {
            $partial[] = ['id' => $id, 'title' => $title];
        }
    }
    $result->free();

    $matches = count($exact) > 0 ? $exact : $partial;
    if (count($matches) !== 1) {
        $summary = [];
        foreach ($matches as $match) {
            $summary[] = $match['id'] . ':' . $match['title'];
        }
        bs_fail('Cannot uniquely identify offer information_id. Matches=' . implode(' | ', $summary));
    }

    bs_log('offer_information_id', (string)$matches[0]['id']);
    bs_log('offer_current_title', $matches[0]['title']);
    return (int)$matches[0]['id'];
}

function bs_backup_offer_row(mysqli $db, string $table, int $informationId, string $backupDir): void {
    $stmt = $db->prepare('SELECT information_id, language_id, title, description FROM ' . $table . ' WHERE information_id = ? AND language_id = ? LIMIT 1');
    $languageId = OFFER_LANGUAGE_ID;
    $stmt->bind_param('ii', $informationId, $languageId);
    $stmt->execute();
    $row = $stmt->get_result()->fetch_assoc();
    $stmt->close();

    if (!$row) {
        bs_fail('Offer row disappeared before backup');
    }

    $backupPath = bs_join($backupDir, 'db/information_description_' . $informationId . '_lang' . OFFER_LANGUAGE_ID . '.json');
    $backupParent = dirname($backupPath);
    if (!is_dir($backupParent) && !mkdir($backupParent, 0755, true)) {
        bs_fail('Cannot create DB backup directory: ' . $backupParent);
    }

    $payload = [
        'backup_time' => date('c'),
        'table' => $table,
        'row' => $row,
        'description_sha256' => hash('sha256', (string)$row['description']),
    ];

    $json = json_encode($payload, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES | JSON_INVALID_UTF8_SUBSTITUTE);
    if (!is_string($json) || file_put_contents($backupPath, $json . PHP_EOL, LOCK_EX) === false) {
        bs_fail('Cannot write DB backup JSON');
    }
    bs_log('backup_db_row', $backupPath);
}

function bs_update_offer(mysqli $db, string $table, int $informationId, string $offerHtml): void {
    $languageId = OFFER_LANGUAGE_ID;
