function bs_log(string $key, $value = null): void {
    if ($value === null || $value === '') {
        echo $key . PHP_EOL;
        return;
    }
    if (is_bool($value)) {
        $value = $value ? 'yes' : 'no';
    }
    echo $key . '=' . (string)$value . PHP_EOL;
}

function bs_fail(string $message): void {
    throw new RuntimeException($message);
}

function bs_short(string $value, int $max = 220): string {
    $value = preg_replace('/\s+/', ' ', trim($value)) ?? '';
    if (strlen($value) <= $max) {
        return $value;
    }
    return substr($value, 0, $max - 3) . '...';
}

function bs_join(string $base, string $relative): string {
    return rtrim($base, DIRECTORY_SEPARATOR) . DIRECTORY_SEPARATOR . str_replace(['/', '\\'], DIRECTORY_SEPARATOR, $relative);
}

function bs_backup_file(string $path, string $backupDir, string $relative): string {
    if (!is_file($path)) {
        bs_fail('Missing file for backup: ' . $path);
    }

    $backupPath = bs_join($backupDir, $relative . '.bak');
    $backupParent = dirname($backupPath);
    if (!is_dir($backupParent) && !mkdir($backupParent, 0755, true)) {
        bs_fail('Cannot create backup directory: ' . $backupParent);
    }
    if (!copy($path, $backupPath)) {
        bs_fail('Cannot backup file: ' . $path);
    }

    bs_log('backup_file', $backupPath);
    return $backupPath;
}

function bs_safe_table(string $prefix, string $name): string {
    $table = $prefix . $name;
    if (!preg_match('/^[A-Za-z0-9_]+$/', $table)) {
        bs_fail('Unsafe DB table name from DB_PREFIX: ' . $table);
    }
    return $table;
}

function bs_lower(string $value): string {
    if (function_exists('mb_strtolower')) {
        return mb_strtolower($value, 'UTF-8');
    }
    return strtolower($value);
}

function bs_contains_offer(string $value): bool {
    return strpos(bs_lower($value), 'оферт') !== false;
}

function bs_syntax_check_self(): string {
    if (!function_exists('shell_exec')) {
        return 'skipped: shell_exec unavailable';
    }

    $php = PHP_BINARY ?: 'php';
    $output = shell_exec(escapeshellarg($php) . ' -l ' . escapeshellarg(__FILE__) . ' 2>&1');
    return is_string($output) ? bs_short($output, 300) : 'failed: no output';
}

